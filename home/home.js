//////////////////////////////////////////////////
// 🏠 HOME UI LOGIC
//////////////////////////////////////////////////

console.log("🏠 home.js loaded");

//////////////////////////////////////////////////
// 🧠 FUNCTIONS SYSTEM
//////////////////////////////////////////////////

window.FUNCTIONS =
  window.FUNCTIONS || {};

//////////////////////////////////////////////////
// 🚀 INIT HOME
//////////////////////////////////////////////////

window.FUNCTIONS.initHome =
async function(){

  try{

    console.log(
      "🏠 initHome"
    );

    //////////////////////////////////////////////////
    // 🛡️ FEED
    //////////////////////////////////////////////////

    const feed =
      document.getElementById(
        "feed"
      );

    if(!feed){

      console.warn(
        "⚠️ feed missing"
      );

      return;

    }

    //////////////////////////////////////////////////
    // ♻️ PREVENT DOUBLE LOAD
    //////////////////////////////////////////////////

    if(feed.dataset.loading){

      console.log(
        "⚠️ already loading"
      );

      return;

    }

    feed.dataset.loading =
      "true";

    //////////////////////////////////////////////////
    // 🧹 CLEAN OLD FEED
    //////////////////////////////////////////////////

    feed.innerHTML = "";

    //////////////////////////////////////////////////
    // 🚀 LOAD REAL FEED
    //////////////////////////////////////////////////

    if(
      typeof window.loadFeed ===
      "function"
    ){

      await window.loadFeed();

    }else{

      console.warn(
        "⚠️ loadFeed missing"
      );

    }

    //////////////////////////////////////////////////
    // ✅ DONE
    //////////////////////////////////////////////////

    delete feed.dataset.loading;

    console.log(
      "✅ Home ready"
    );

  }catch(e){

    //////////////////////////////////////////////////
    // 💀 ERROR
    //////////////////////////////////////////////////

    console.error(
      "❌ initHome error:",
      e
    );

    //////////////////////////////////////////////////
    // 🛡️ RESET STATE
    //////////////////////////////////////////////////

    const feed =
      document.getElementById(
        "feed"
      );

    if(feed){

      delete feed.dataset.loading;

    }

  }

};