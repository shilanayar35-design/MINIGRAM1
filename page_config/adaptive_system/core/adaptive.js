//////////////////////////////////////////////////
// 📦 LOAD ADAPTIVE FILE
//////////////////////////////////////////////////

window.loadAdaptive =
async function(id){

  //////////////////////////////////////////////////
  // ♻️ ALREADY LOADED
  //////////////////////////////////////////////////

  if(window.ADAPTIVE?.[id])
    return;

  //////////////////////////////////////////////////
  // 📜 CREATE SCRIPT
  //////////////////////////////////////////////////

  const s =
    document.createElement("script");

  s.src =
    `page_config/adaptive_system/adaptive_loader_${id}.js`;

  //////////////////////////////////////////////////
  // ❌ ERROR
  //////////////////////////////////////////////////

  s.onerror = () => {

    console.error(
      "❌ Adaptive load fail:",
      id
    );

  };

  //////////////////////////////////////////////////
  // 📦 APPEND
  //////////////////////////////////////////////////

  document.body.appendChild(s);

  //////////////////////////////////////////////////
  // ⏳ WAIT
  //////////////////////////////////////////////////

  await new Promise(r => {

    s.onload = r;

  });

  console.log(
    "✅ Adaptive loaded:",
    id
  );

};

//////////////////////////////////////////////////
// 🎨 APPLY CSS ADAPTIVE
//////////////////////////////////////////////////

window.applyAdaptive =
async function(page){

  //////////////////////////////////////////////////
  // 📦 PAGE CONFIG
  //////////////////////////////////////////////////

  const config =
    window.PAGE_CONFIG?.[page];

  //////////////////////////////////////////////////
  // ❌ NO ADAPTIVE
  //////////////////////////////////////////////////

  if(!config?.adaptive)
    return;

  //////////////////////////////////////////////////
  // 🧠 ADAPTIVE ID
  //////////////////////////////////////////////////

  const id =
    config.adaptive;

  //////////////////////////////////////////////////
  // 📦 LOAD FILE
  //////////////////////////////////////////////////

  await window.loadAdaptive(id);

  //////////////////////////////////////////////////
  // 📦 DATA
  //////////////////////////////////////////////////

  const data =
    window.ADAPTIVE?.[id];

  if(!data)
    return;

  //////////////////////////////////////////////////
  // 🌍 ROOT
  //////////////////////////////////////////////////

  const root =
    document.documentElement;

  //////////////////////////////////////////////////
  // 🔥 APPLY VARIABLES
  //////////////////////////////////////////////////

  for(const file in data){

    //////////////////////////////////////////////////
    // 📦 MODE DATA
    //////////////////////////////////////////////////

    const modeData =
      data[file]?.[
        window.performanceMode
      ];

    if(!modeData)
      continue;

    //////////////////////////////////////////////////
    // 🎨 APPLY
    //////////////////////////////////////////////////

    for(const key in modeData){

      //////////////////////////////////////////////////
      // ⚡ ONLY CSS VARS
      //////////////////////////////////////////////////

      if(
        typeof modeData[key] !==
        "object"
      ){

        root.style.setProperty(

          `--${key}`,

          modeData[key]

        );

      }

    }

  }

  console.log(

    "🎨 Adaptive applied:",

    page,

    "|",

    window.performanceMode

  );

};

//////////////////////////////////////////////////
// ⚡ GET JS ADAPTIVE DATA
//////////////////////////////////////////////////

window.getAdaptiveData =
function(file){

  //////////////////////////////////////////////////
  // 📄 CURRENT PAGE
  //////////////////////////////////////////////////

  const page =
    window.CURRENT_PAGE;

  //////////////////////////////////////////////////
  // 📦 CONFIG
  //////////////////////////////////////////////////

  const config =
    window.PAGE_CONFIG?.[page];

  //////////////////////////////////////////////////
  // ❌ NO CONFIG
  //////////////////////////////////////////////////

  if(!config?.adaptive)
    return {};

  //////////////////////////////////////////////////
  // 🧠 ADAPTIVE ID
  //////////////////////////////////////////////////

  const adaptiveId =
    config.adaptive;

  //////////////////////////////////////////////////
  // 📦 FILE DATA
  //////////////////////////////////////////////////

  const data =
    window.ADAPTIVE?.[
      adaptiveId
    ]?.[file];

  //////////////////////////////////////////////////
  // ❌ NO DATA
  //////////////////////////////////////////////////

  if(!data)
    return {};

  //////////////////////////////////////////////////
  // 🚀 RETURN MODE DATA
  //////////////////////////////////////////////////

  return (

    data[
      window.performanceMode
    ] || {}

  );

};

