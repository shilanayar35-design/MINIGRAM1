//////////////////////////////////////////////////
// 📦 PAGE REGISTRY (FINAL UPGRADE)
//////////////////////////////////////////////////

console.log(
  "🚀 pageRegistry.js loaded"
);

//////////////////////////////////////////////////
// 📦 GLOBAL PAGE CONFIG STORAGE
//////////////////////////////////////////////////

window.PAGE_CONFIG =
  window.PAGE_CONFIG || {};

//////////////////////////////////////////////////
// 🚀 REGISTER PAGE CONFIG
//////////////////////////////////////////////////

window.registerPageConfig =
function(name, config){

  try{

    //////////////////////////////////////////////////
    // 🛡️ VALIDATE
    //////////////////////////////////////////////////

    if(!name){

      console.warn(
        "⚠️ Config name missing"
      );

      return;

    }

    if(!config){

      console.warn(
        `⚠️ Config object missing: ${name}`
      );

      return;

    }

    //////////////////////////////////////////////////
    // 💾 SAVE CONFIG
    //////////////////////////////////////////////////

    window.PAGE_CONFIG[name] =
      config;

    //////////////////////////////////////////////////
    // ✅ SUCCESS
    //////////////////////////////////////////////////

    console.log(
      `✅ Config Registered: ${name}`
    );

  }catch(e){

    //////////////////////////////////////////////////
    // ❌ ERROR
    //////////////////////////////////////////////////

    console.error(
      "❌ registerPageConfig error:",
      e
    );

  }

};

//////////////////////////////////////////////////
// 🔍 GET CONFIG
//////////////////////////////////////////////////

window.getPageConfig =
function(name){

  return (
    window.PAGE_CONFIG?.[name]
  );

};

//////////////////////////////////////////////////
// 📋 GET ALL CONFIGS
//////////////////////////////////////////////////

window.getAllPageConfigs =
function(){

  return (
    window.PAGE_CONFIG || {}
  );

};

//////////////////////////////////////////////////
// 🧪 DEBUG
//////////////////////////////////////////////////

window.debugPageConfig =
function(name){

  console.log(

    "📦 PAGE CONFIG:",

    name,

    window.PAGE_CONFIG?.[name]

  );

};

//////////////////////////////////////////////////
// 🛡️ GLOBAL ERROR LOGGER
//////////////////////////////////////////////////

if(
  !window.__PAGE_REGISTRY_ERROR
){

  window.__PAGE_REGISTRY_ERROR =
    true;

  window.addEventListener?.(

    "error",

    e => {

      console.log(

        "💀 PAGE REGISTRY ERROR:",

        e.filename,

        e.message,

        "LINE:",

        e.lineno

      );

    }

  );

}

