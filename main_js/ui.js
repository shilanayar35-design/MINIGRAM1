//////////////////////////////////////////////////
// 🧹 GLOBAL PAGE CLEANER
//////////////////////////////////////////////////

window.cleanupPage =
async function(){

  try{

    //////////////////////////////////////////////////
    // 📄 CURRENT PAGE
    //////////////////////////////////////////////////

    const page =
      window.CURRENT_PAGE;

    if(!page)
      return;

    //////////////////////////////////////////////////
    // 📦 DESTROY FUNCTION NAME
    //////////////////////////////////////////////////

    const destroyName =
      page + "Destroy";

    //////////////////////////////////////////////////
    // 📦 FUNCTION
    //////////////////////////////////////////////////

    const destroyFn =

      window.FUNCTIONS?.[
        destroyName
      ];

    //////////////////////////////////////////////////
    // 🚀 RUN DESTROY
    //////////////////////////////////////////////////

    if(
      typeof destroyFn ===
      "function"
    ){

      await destroyFn();

      console.log(
        `🧹 ${page} cleaned`
      );

    }

  }catch(e){

    console.error(
      "❌ cleanupPage error:",
      e
    );

  }

};

