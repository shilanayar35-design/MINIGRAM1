window.safeHide =
function(id){

  const el =
    document.getElementById(id);

  if(el){

    el.style.display =
      "none";

  }

};

window.safeShow =
function(id){

  const el =
    document.getElementById(id);

  if(el){

    el.style.display =
      "";

  }

};

