//////////////////////////////////////////////////
// 📦 CONFIG
//////////////////////////////////////////////////

const BATCH_SIZE =

  window.IS_LOW_END
  ? 5
  : 10;

//////////////////////////////////////////////////
// 🌍 GLOBAL STATE
//////////////////////////////////////////////////

window.FEED_RENDER_STATE =
window.FEED_RENDER_STATE || {

  posts: [],
  page: 0,
  loading: false,
  ended: false

};

//////////////////////////////////////////////////
// 🚀 MAIN RENDER FEED
//////////////////////////////////////////////////

function renderFeed(
  posts = [],
  append = true,
  page = 0
){

  //////////////////////////////////////////////////
  // 📦 FEED
  //////////////////////////////////////////////////

  const feed =
    document.getElementById(
      "feed"
    );

  const template =
    document.getElementById(
      "postTemplate"
    );

  //////////////////////////////////////////////////
  // 🛑 HARD SAFETY
  //////////////////////////////////////////////////

  if(
    !feed ||
    !template
  ){

    console.error(
      "❌ feed/template missing"
    );

    return;

  }

  if(
    !Array.isArray(posts)
  ){

    return;

  }

  //////////////////////////////////////////////////
  // 🧹 REMOVE EMPTY STATE
  //////////////////////////////////////////////////

  document
    .getElementById(
      "emptyFeed"
    )
    ?.remove();

  //////////////////////////////////////////////////
  // 📦 BATCH CALCULATION
  //////////////////////////////////////////////////

  const start =
    page * BATCH_SIZE;

  const end =
    start + BATCH_SIZE;

  //////////////////////////////////////////////////
  // ✂️ ONLY RENDER BATCH
  //////////////////////////////////////////////////

  const batch =
    posts.slice(
      start,
      end
    );

  //////////////////////////////////////////////////
  // 🛑 NO POSTS
  //////////////////////////////////////////////////

  if(!batch.length){

    console.log(
      "⚠️ No more posts"
    );

    window.FEED_RENDER_STATE
      .ended = true;

    return;

  }

  //////////////////////////////////////////////////
  // 🚀 DOCUMENT FRAGMENT
  //////////////////////////////////////////////////

  const fragment =
    document.createDocumentFragment();

  //////////////////////////////////////////////////
  // 🚀 RENDER POSTS
  //////////////////////////////////////////////////

  batch.forEach(post => {

    //////////////////////////////////////////////////
    // 🛡️ SAFETY
    //////////////////////////////////////////////////

    if(
      !post ||
      !post.id
    ) return;

    //////////////////////////////////////////////////
    // ♻️ DUPLICATE CHECK
    //////////////////////////////////////////////////

    if(

      document.getElementById(
        "post-" + post.id
      )

    ) return;

    //////////////////////////////////////////////////
    // 👤 USER DATA
    //////////////////////////////////////////////////

    const username =
      post.username ||
      "user";

    //////////////////////////////////////////////////
    // 🖼️ AVATAR
    //////////////////////////////////////////////////

    const avatar =

      post.avatar_url ||

      `https://i.pravatar.cc/150?u=${
        encodeURIComponent(
          username
        )
      }`;

    //////////////////////////////////////////////////
    // 🖼️ IMAGE
    //////////////////////////////////////////////////

    const image =
      post.image_url ||
      "";

    //////////////////////////////////////////////////
    // 📝 CAPTION
    //////////////////////////////////////////////////

    const caption =
      post.caption ||
      "";

    //////////////////////////////////////////////////
    // ❤️ LIKES
    //////////////////////////////////////////////////

    const likes =

      Number(
        post.likes
      ) || 0;

    //////////////////////////////////////////////////
    // 🕒 TIME
    //////////////////////////////////////////////////

    const time =

      (
        typeof window.formatTime ===
        "function" &&

        post.created_at
      )

      ? window.formatTime(
          post.created_at
        )

      : "";

    //////////////////////////////////////////////////
    // 📄 CLONE TEMPLATE
    //////////////////////////////////////////////////

    const clone =

      template.content
      .cloneNode(true);

    //////////////////////////////////////////////////
    // 📦 MAIN POST
    //////////////////////////////////////////////////

    const el =
      clone.querySelector(
        ".post"
      );

    if(!el) return;

    //////////////////////////////////////////////////
    // 🆔 POST ID
    //////////////////////////////////////////////////

    el.id =
      "post-" + post.id;

    //////////////////////////////////////////////////
    // ⚡ FAST ELEMENTS
    //////////////////////////////////////////////////

    const avatarEl =
      clone.querySelector(
        ".avatar"
      );

    const usernameEl =
      clone.querySelector(
        ".username"
      );

    const img =
      clone.querySelector(
        ".postImage"
      );

    const likesEl =
      clone.querySelector(
        ".postLikes"
      );

    const timeEl =
      clone.querySelector(
        ".postTime"
      );

    const captionUser =
      clone.querySelector(
        ".postCaption b"
      );

    const captionText =
      clone.querySelector(
        ".captionText"
      );

    const likeBtn =
      clone.querySelector(
        ".likeBtn"
      );

    const commentBtn =
      clone.querySelector(
        ".commentBtn"
      );

    const shareBtn =
      clone.querySelector(
        ".shareBtn"
      );

    const saveBtn =
      clone.querySelector(
        ".saveBtn"
      );

    //////////////////////////////////////////////////
    // 👤 USER
    //////////////////////////////////////////////////

    avatarEl.src =
      avatar;

    usernameEl.textContent =
      username;

    //////////////////////////////////////////////////
    // 🖼️ IMAGE OPTIMIZATION
    //////////////////////////////////////////////////

    img.loading =
      "lazy";

    img.decoding =
      "async";

    img.src =
      image;

    img.dataset.id =
      post.id;

    //////////////////////////////////////////////////
    // 🖼️ IMAGE LOADED
    //////////////////////////////////////////////////

    img.onload = ()=>{

      img.classList.remove(
        "loading"
      );

      img.classList.add(
        "loaded"
      );

    };

    //////////////////////////////////////////////////
    // ❌ IMAGE ERROR
    //////////////////////////////////////////////////

    img.onerror = ()=>{

      img.src =
        "https://via.placeholder.com/400x400?text=No+Image";

    };

    //////////////////////////////////////////////////
    // ❤️ LIKES
    //////////////////////////////////////////////////

    likesEl.textContent =
      likes + " likes";

    likesEl.id =
      "likes-" + post.id;

    //////////////////////////////////////////////////
    // 🕒 TIME
    //////////////////////////////////////////////////

    timeEl.textContent =
      time;

    //////////////////////////////////////////////////
    // 📝 CAPTION
    //////////////////////////////////////////////////

    captionUser.textContent =
      username;

    captionText.textContent =
      caption;

    //////////////////////////////////////////////////
    // ❤️ DOUBLE TAP LIKE
    //////////////////////////////////////////////////

    let lastTap = 0;

    img?.addEventListener(
      "click",
      ()=>{

        const now =
          Date.now();

        if(
          now - lastTap < 300
        ){

          //////////////////////////////////////////////////
          // ❤️ HEART
          //////////////////////////////////////////////////

          window.animateHeart?.(
            img
          );

          //////////////////////////////////////////////////
          // ❤️ LIKE
          //////////////////////////////////////////////////

          window.likePost?.(
            likeBtn,
            post.id
          );

        }

        lastTap = now;

      },
      {
        passive: true
      }
    );

    //////////////////////////////////////////////////
    // ❤️ LIKE BUTTON
    //////////////////////////////////////////////////

    likeBtn?.addEventListener(
      "click",
      ()=>{

        window.likePost?.(
          likeBtn,
          post.id
        );

      },
      {
        passive: true
      }
    );

    //////////////////////////////////////////////////
    // 💬 COMMENT
    //////////////////////////////////////////////////

    commentBtn?.addEventListener(
      "click",
      ()=>{

        window.commentPost?.(
          post.id
        );

      },
      {
        passive: true
      }
    );

    //////////////////////////////////////////////////
    // 📤 SHARE
    //////////////////////////////////////////////////

    shareBtn?.addEventListener(
      "click",
      ()=>{

        window.sharePost?.(
          image
        );

      },
      {
        passive: true
      }
    );

    //////////////////////////////////////////////////
    // 💾 SAVE
    //////////////////////////////////////////////////

    saveBtn?.addEventListener(
      "click",
      ()=>{

        window.savePost?.(
          post.id
        );

      },
      {
        passive: true
      }
    );

    //////////////////////////////////////////////////
    // 🚀 ADD TO FRAGMENT
    //////////////////////////////////////////////////

    append

      ? fragment.appendChild(clone)

      : fragment.prepend(clone);

  });

  //////////////////////////////////////////////////
  // 🚀 SINGLE DOM INSERT
  //////////////////////////////////////////////////

  requestAnimationFrame(()=>{

    append

      ? feed.appendChild(fragment)

      : feed.prepend(fragment);

  });

  //////////////////////////////////////////////////
  // 🧹 AUTO CLEAN POSTS
  //////////////////////////////////////////////////

  const LIMIT =

    window.IS_LOW_END
    ? 10
    : 20;

  while(
    feed.children.length > LIMIT
  ){

    feed.removeChild(
      feed.firstElementChild
    );

  }

  //////////////////////////////////////////////////
  // 📊 STATE UPDATE
  //////////////////////////////////////////////////

  window.FEED_RENDER_STATE.page =
    page;

  console.log(

    "✅ Batch Rendered:",

    page,

    "| Posts:",

    batch.length

  );

}

//////////////////////////////////////////////////
// 🚀 LOAD NEXT BATCH
//////////////////////////////////////////////////

window.loadMoreFeed =
function(){

  //////////////////////////////////////////////////
  // 🛑 END CHECK
  //////////////////////////////////////////////////

  if(
    window.FEED_RENDER_STATE
    .ended
  ){

    return;

  }

  //////////////////////////////////////////////////
  // 🛑 LOADING CHECK
  //////////////////////////////////////////////////

  if(
    window.FEED_RENDER_STATE
    .loading
  ){

    return;

  }

  //////////////////////////////////////////////////
  // 🚀 START
  //////////////////////////////////////////////////

  window.FEED_RENDER_STATE
    .loading = true;

  //////////////////////////////////////////////////
  // 📦 NEXT PAGE
  //////////////////////////////////////////////////

  const nextPage =

    window.FEED_RENDER_STATE
    .page + 1;

  //////////////////////////////////////////////////
  // 💤 IDLE RENDER
  //////////////////////////////////////////////////

  requestIdleCallback(()=>{

    renderFeed(

      window.FEED_RENDER_STATE
      .posts,

      true,

      nextPage

    );

    //////////////////////////////////////////////////
    // ✅ DONE
    //////////////////////////////////////////////////

    window.FEED_RENDER_STATE
      .loading = false;

  });

};

//////////////////////////////////////////////////
// 🚀 AUTO SCROLL LOAD
//////////////////////////////////////////////////

window.initFeedInfiniteScroll =
function(){

  //////////////////////////////////////////////////
  // 🛑 REMOVE OLD
  //////////////////////////////////////////////////

  window.removeEventListener(
    "scroll",
    window.__FEED_SCROLL_HANDLER
  );

  //////////////////////////////////////////////////
  // 🚀 NEW HANDLER
  //////////////////////////////////////////////////

  window.__FEED_SCROLL_HANDLER =
  function(){

    //////////////////////////////////////////////////
    // 📜 SCROLL CHECK
    //////////////////////////////////////////////////

    const nearBottom =

      window.innerHeight +
      window.scrollY >=

      document.body.offsetHeight
      - 1200;

    //////////////////////////////////////////////////
    // 🚀 LOAD MORE
    //////////////////////////////////////////////////

    if(nearBottom){

      window.loadMoreFeed?.();

    }

  };

  //////////////////////////////////////////////////
  // 🚀 ADD EVENT
  //////////////////////////////////////////////////

  window.addEventListener(

    "scroll",

    window.__FEED_SCROLL_HANDLER,

    {
      passive: true
    }

  );

};

//////////////////////////////////////////////////
// 🌍 GLOBAL EXPORT
//////////////////////////////////////////////////

window.renderFeed =
renderFeed;

